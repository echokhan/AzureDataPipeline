CREATE TABLE raw.batch_weatherdata (
    id INT IDENTITY(1,1) PRIMARY KEY,  -- Auto-increment column
    dt BIGINT,
    dt_iso NVARCHAR(50),
    timezone INT,
    city_name NVARCHAR(50),
    lat FLOAT,
    lon FLOAT,
    temp FLOAT,
    visibility INT,
    dew_point FLOAT,
    feels_like FLOAT,
    temp_min FLOAT,
    temp_max FLOAT,
    pressure INT,
    sea_level INT NULL,
    grnd_level INT NULL,
    humidity INT,
    wind_speed FLOAT,
    wind_deg INT,
    wind_gust FLOAT NULL,
    rain_1h FLOAT NULL,
    rain_3h FLOAT NULL,
    snow_1h FLOAT NULL,
    snow_3h FLOAT NULL,
    clouds_all INT,
    weather_id INT,
    weather_main NVARCHAR(50),
    weather_description NVARCHAR(50),
    weather_icon NVARCHAR(10),
    created_at DATETIME DEFAULT (GETUTCDATE())  -- Timestamp column with default UTC value
);